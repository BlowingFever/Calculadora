using System.Windows;

// Tells WPF where to locate theme-specific and generic resource dictionaries.
// ResourceDictionaryLocation.None            – no per-theme folder dictionaries.
// ResourceDictionaryLocation.SourceAssembly  – Generic.xaml is embedded in this assembly.
